# four level in testing
# - unit testing
# - integration testing
# - system testing
# - acceptance testing
# 
# unit/component testing
# - components are low-level
# - developers write tests while writing code
# 
# integration testing
# - combines the unit tests and test the flow of data from one component to another
# 
# system testing
# - which tests all the software you tested against the set requirements and expectations to ensure completeness
# - reliability
# - performance
# - security
# - load balancing
# 
# acceptance testing